private void carregarAluno(){

    DefaultTableModel modelo =
            new DefaultTableModel();

   String pesquisa =
            txtPesquisa.getText().toLowerCase();

    modelo.addColumn("ID");
    modelo.addColumn("Nome");
    modelo.addColumn("Turma");
    modelo.addColumn("Email");

    for (Aluno aluno :
            BancoSimuladoAluno.alunos){

        if (aluno.getNome()
                .toLowerCase()
                .contains(pesquisa)){

            modelo.addRow(new Object[]{
                aluno.getId(),
                aluno.getNome(),
                aluno.getTurma(),
                aluno.getEmail()
            });
        }
    }

    tabelaAlunos.setModel(modelo);
}
   
    
    private void limparCampos() {

    txtNome.setText("");
    txtTurma.setText("");
    txtEmail.setText("");

    idSelecionado = 0;

    txtNome.requestFocus();
    }
    
