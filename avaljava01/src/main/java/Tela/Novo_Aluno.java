    private void tabelaAlunosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaAlunosMouseClicked
    
        int linha =
            tabelaAlunos.getSelectedRow();

    if (linha >= 0) {

        idSelecionado = Integer.parseInt(
                tabelaAlunos
                .getValueAt(linha, 0)
                .toString());

        
        txtNome.setText(
                tabelaAlunos
                .getValueAt(linha, 1)
                .toString());

        txtTurma.setText(
                tabelaAlunos
                .getValueAt(linha, 2)
                .toString());

        txtEmail.setText(
                tabelaAlunos
                .getValueAt(linha, 3)
                .toString());
    }

    }//GEN-LAST:event_tabelaAlunosMouseClicked

  

